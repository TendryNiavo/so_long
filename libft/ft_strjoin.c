#include "libft.h"

char	*ft_strjoin(char const	*s1, char const	*s2)
{
	char			*dest;
	unsigned int	len;
	unsigned int	i;
	unsigned int	j;

	len = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	dest = (char *)malloc((len + 1) * sizeof (char));
	if (!dest)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		dest[i] = s2[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
